class SpecialCharactersNotations {
  public static void main(String[] args) {
    System.out.print(String.format("backspace \\b: %c\n", '\b'));
    System.out.print(String.format("carriage return \\r: \n", '\r'));
    System.out.print(String.format("new page \\f: \n", '\f'));
    System.out.print(String.format("new line \\n: \n", '\n'));
    System.out.print(String.format("horizontal tabulation \\t: \n", '\t'));
    // System.out.print(String.format("vertical tabulation \\v: \n", '\v'));
    // System.out.print(String.format("sound signal \\a: \n", '\a'));
  }
}
