#!/usr/bin/python3

a = "hello"; b = "world"
c = a + b
print(f"{a} + {b} = {c}\n(a: {type(a)}, b: {type(b)}, c: {type(c)})\n")

a = 1; b = 2
c = a + b
print(f"{a} + {b} = {c}\n(a: {type(a)}, b: {type(b)}, c: {type(c)})\n")

a = [1, 3, 5]; b = [2, 4]
c = a + b;
print(f"{a} + {b} = {c}\n(a: {type(a)}, b: {type(b)}, c: {type(c)})\n")
