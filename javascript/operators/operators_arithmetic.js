#!/usr/bin/env node

let a = 6, b = 2, c = 0;

c = a + b;
console.log(`${a} + ${b} = ${c}`);
c = a - b;
console.log(`${a} - ${b} = ${c}`);
c = a * b;
console.log(`${a} * ${b} = ${c}`);
c = a / b;
console.log(`${a} / ${b} = ${c}`);
c = a % b;
console.log(`${a} % ${b} = ${c}`);

console.log(`-${a} = ${-a}`);
console.log(`+${b} = ${+b}`);
