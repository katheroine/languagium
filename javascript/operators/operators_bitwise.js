#!/usr/bin/env node

let a = 10, b = 12;

c = a & b;
console.log(`${a} & ${b} = ${c}`); // 0000 0000 0000 0000 0000 0000 0000 1000 (8)
c = a | b;
console.log(`${a} | ${b} = ${c}`); // 0000 0000 0000 0000 0000 0000 0000 1110 (14)
c = a ^ b;
console.log(`${a} ^ ${b} = ${c}`); // 0000 0000 0000 0000 0000 0000 0000 0110 (6)

c = ~a;
console.log(`~${a} = ${c}`); // 1111 1111 1111 1111 1111 1111 1111 0101 (-11)
c = ~b;
console.log(`~${b} = ${~b}`); // 1111 1111 1111 1111 1111 1111 1111 0011 (-13)
