$c = $a + $b;
print("{$a} + {$b} = {$c}\n");
$c = $a - $b;
print("{$a} - {$b} = {$c}\n");
$c = $a * $b;
print("{$a} * {$b} = {$c}\n");
$c = $a / $b;
print("{$a} / {$b} = {$c}\n");
$c = $a % $b;
print("{$a} % {$b} = {$c}\n");
