#!/usr/bin/php
<?php

$a = 10; $b = 12;

$c = $a & $b;
print("{$a} & {$b} = {$c}\n"); // 0000 0000 0000 0000 0000 0000 0000 1000 // 8
$c = $a | $b;
print("{$a} | {$b} = {$c}\n"); // 0000 0000 0000 0000 0000 0000 0000 1110 // 14
$c = $a ^ $b;
print("{$a} ^ {$b} = {$c}\n"); // 0000 0000 0000 0000 0000 0000 0000 0110 // 6

print("~{$a} = " . (~$a) . "\n"); // 1111 1111 1111 1111 1111 1111 1111 0101 // -11
print("~{$b} = " . (~$b) . "\n"); // 1111 1111 1111 1111 1111 1111 1111 0011 // -13
